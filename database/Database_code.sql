CREATE DATABASE LOGISTICS_COMPANY;
USE LOGISTICS_COMPANY;

CREATE TABLE CUSTOMER (
    customer_id   INT            NOT NULL AUTO_INCREMENT ,
    password_hash VARCHAR(255)   NOT NULL,
    name          VARCHAR(255)   NOT NULL,
    phone         VARCHAR(50),
    email         VARCHAR(255)   UNIQUE,
    address       VARCHAR(500),
    CONSTRAINT PK_CUSTOMER PRIMARY KEY (customer_id)
);

CREATE TABLE C_ORDER (
    order_id      BIGINT          NOT NULL AUTO_INCREMENT,
    sender_id     INT             NOT NULL,
    receiver_id   INT             NOT NULL,
    weight        FLOAT,
    length        FLOAT,
    width         FLOAT,
    height        FLOAT,
    type          VARCHAR(100),
    status        VARCHAR(100),
    price         DECIMAL(10,2)  NOT NULL,
    CONSTRAINT PK_C_ORDER    PRIMARY KEY (order_id),
    CONSTRAINT FK_ORDER_SENDER   FOREIGN KEY (sender_id)   REFERENCES CUSTOMER (customer_id),
    CONSTRAINT FK_ORDER_RECEIVER FOREIGN KEY (receiver_id) REFERENCES CUSTOMER (customer_id)
);

CREATE TABLE DRIVER (
    driver_id      INT           NOT NULL AUTO_INCREMENT,
    password_hash VARCHAR(255)   NOT NULL,
    name           VARCHAR(255),
    phone         VARCHAR(50),
    email         VARCHAR(255)   UNIQUE,
    license_class  VARCHAR(10),
    working_hours  FLOAT,
    status         VARCHAR(50),
    CONSTRAINT PK_DRIVER PRIMARY KEY (driver_id)
);

CREATE TABLE VEHICLE (
    vehicle_id        INT           NOT NULL AUTO_INCREMENT,
    type              VARCHAR(100),
    license_plate	VARCHAR(50)  UNIQUE,
    vmax_weight       FLOAT,
    vmax_volume       FLOAT,
    status            VARCHAR(50),
    current_location  VARCHAR(255),
    CONSTRAINT PK_VEHICLE PRIMARY KEY (vehicle_id)
);

CREATE TABLE DELIVERY (
    delivery_id      INT          NOT NULL AUTO_INCREMENT,
    order_id         BIGINT       NOT NULL,
    assignment_id    INT,
    status           VARCHAR(100),
    scheduled_time   DATETIME,
    completed_time   DATETIME,
    CONSTRAINT PK_DELIVERY PRIMARY KEY (delivery_id),
    CONSTRAINT FK_DEL_ORDER FOREIGN KEY (order_id) REFERENCES C_ORDER(order_id)
);

CREATE TABLE DRIVER_VEHICLE_ASSIGNMENT (
    assignment_id  INT       NOT NULL AUTO_INCREMENT,
    driver_id      INT       NOT NULL,
    vehicle_id     INT       NOT NULL,
    route_id       INT,
    start_time     DATETIME,
    end_time       DATETIME,
    status         VARCHAR(50),
    CONSTRAINT PK_DVA         PRIMARY KEY (assignment_id),
    CONSTRAINT FK_DVA_DRIVER  FOREIGN KEY (driver_id)  REFERENCES DRIVER  (driver_id),
    CONSTRAINT FK_DVA_VEHICLE FOREIGN KEY (vehicle_id) REFERENCES VEHICLE (vehicle_id)
);

ALTER TABLE DELIVERY
    ADD CONSTRAINT FK_DEL_ASSIGNMENT
    FOREIGN KEY (assignment_id) REFERENCES DRIVER_VEHICLE_ASSIGNMENT (assignment_id);

CREATE TABLE ROUTE (
    route_id        INT      NOT NULL AUTO_INCREMENT,
    optimized_path  TEXT,
    total_distance  FLOAT,
    estimated_time  FLOAT,
    CONSTRAINT PK_ROUTE     PRIMARY KEY (route_id)
);

ALTER TABLE DRIVER_VEHICLE_ASSIGNMENT
    ADD CONSTRAINT FK_DVA_ROUTE
    FOREIGN KEY (route_id) REFERENCES ROUTE (route_id);

CREATE TABLE ROUTE_STOP (
    stop_id         INT           NOT NULL,
    route_id        INT           NOT NULL,
    facility_type   VARCHAR(100),
    name            VARCHAR(255),
    location        VARCHAR(255),
    fmax_weight     INT,
    fmax_volume     INT,
    operating_hours VARCHAR(255),
    sequence_order  INT,
    planned_eta     DATETIME,
    actual_arrival  DATETIME,
    scan_status     VARCHAR(100),
    CONSTRAINT PK_ROUTE_STOP      PRIMARY KEY (stop_id),
    CONSTRAINT FK_STOP_ROUTE      FOREIGN KEY (route_id) REFERENCES ROUTE (route_id)
);

CREATE TABLE CONDITION_REPORT (
    report_id      INT           NOT NULL AUTO_INCREMENT ,
    route_id       INT           NOT NULL,
    region         VARCHAR(255),
    weather_status VARCHAR(100),
    road_status    VARCHAR(100),
    risk_score     FLOAT,
    recorded_at    DATETIME,
    CONSTRAINT PK_CONDITION_REPORT   PRIMARY KEY (report_id),
    CONSTRAINT FK_CONDREPORT_ROUTE   FOREIGN KEY (route_id) REFERENCES ROUTE (route_id)
);

CREATE TABLE PAYMENT (
    payment_id         INT            NOT NULL AUTO_INCREMENT ,
    order_id           BIGINT         NOT NULL,
    amount             FLOAT,
    method             VARCHAR(100),
    gateway_reference  VARCHAR(255),
    status             VARCHAR(50),
    timestamp          TIMESTAMP,
    CONSTRAINT PK_PAYMENT       PRIMARY KEY (payment_id),
    CONSTRAINT FK_PAYMENT_ORDER FOREIGN KEY (order_id) REFERENCES C_ORDER (order_id)
);

CREATE TABLE ORDER_UPDATE (
    update_id    INT           NOT NULL AUTO_INCREMENT ,
    order_id     BIGINT           NOT NULL,
    driver_id    INT,
    stop_id      INT,
    update_type  VARCHAR(100),
    scan_type    VARCHAR(100),
    operator_id  VARCHAR(100),
    notes        TEXT,
    new_status   VARCHAR(100),
    updated_at   TIMESTAMP,
    CONSTRAINT PK_ORDER_UPDATE        PRIMARY KEY (update_id),
    CONSTRAINT FK_OUPDATE_ORDER       FOREIGN KEY (order_id)  REFERENCES C_ORDER    (order_id),
    CONSTRAINT FK_OUPDATE_DRIVER      FOREIGN KEY (driver_id) REFERENCES DRIVER      (driver_id),
    CONSTRAINT FK_OUPDATE_STOP        FOREIGN KEY (stop_id)   REFERENCES ROUTE_STOP  (stop_id)
);

CREATE TABLE ADMIN (
    admin_id      INT           NOT NULL AUTO_INCREMENT,
    password_hash VARCHAR(255)  NOT NULL,
    name          VARCHAR(255)  NOT NULL,
    phone         VARCHAR(50),
    email         VARCHAR(255)  UNIQUE,
    CONSTRAINT PK_ADMIN PRIMARY KEY (admin_id)
);

CREATE TABLE ADMIN_OVERRIDE (
    override_id    INT           NOT NULL AUTO_INCREMENT,
    delivery_id    INT           NOT NULL,
    admin_id       INT,
    override_type  VARCHAR(100),
    reason         TEXT,
    old_value      VARCHAR(500),
    new_value      VARCHAR(500),
    created_at     TIMESTAMP,
    CONSTRAINT PK_ADMIN_OVERRIDE      PRIMARY KEY (override_id),
    CONSTRAINT FK_OVERRIDE_DELIVERY   FOREIGN KEY (delivery_id) REFERENCES DELIVERY (delivery_id),
CONSTRAINT FK_OVERRIDE_ADMIN      FOREIGN KEY (admin_id) REFERENCES ADMIN (admin_id)
);

CREATE INDEX idx_customer_name ON CUSTOMER(name);
CREATE INDEX idx_order_status ON C_ORDER(status);
CREATE INDEX idx_payment_status ON PAYMENT(status);
CREATE INDEX idx_orderupdate_id_time ON ORDER_UPDATE(order_id, updated_at);
CREATE INDEX idx_condition_time_risk ON CONDITION_REPORT(recorded_at, risk_score);
CREATE INDEX idx_dva_time ON DRIVER_VEHICLE_ASSIGNMENT(start_time);


